public class javaDataType {
    public static void main(String[] args){
        // primitive data type
        //boolean - can be true / false
        boolean flag = true;
        System.out.println(flag);

        //byte - size 1byte / 8bits && range: -128 to 127
        byte byteValue = 60;
        System.out.println(byteValue);
        //short - size 2byte / 16bits && range: -32768 to  32767
        short shortValue = 1000;
        System.out.println(shortValue);
        //int - size 4byte / 32bits && range: -2^31 to 2^31 -1
        int integerValue = 1268767;
        System.out.println(integerValue);
        //long - size 8byte / 64bits && range: -2^64 to 2^64 -1
        long longValue = 1234569991;
        System.out.println(longValue);

        //float - size 4byte / 32bits
        float floatValue = 5.6F;
        System.out.println(floatValue);
        //double - size 8byte / 64bits
        double doubleValue = 78576.73747;
        System.out.println(doubleValue);

        //char - size 2byte // 16bits
        char charValue = 'A';
        System.out.println(charValue);

    }
}
